import dotenv from "dotenv";
dotenv.config({ path: ".env.local" });

const BASE = process.env.NEXT_PUBLIC_FRAPPE_URL;
const API_KEY = process.env.FRAPPE_API_KEY;
const API_SECRET = process.env.FRAPPE_API_SECRET;

const HEADERS = {
  Authorization: `token ${API_KEY}:${API_SECRET}`,
  "Content-Type": "application/json",
};

async function apiCall(method, path, body) {
  const res = await fetch(`${BASE}${path}`, {
    method,
    headers: HEADERS,
    body: body ? JSON.stringify(body) : undefined,
  });
  const text = await res.text();
  let json;
  try { json = text ? JSON.parse(text) : {}; } catch { json = { raw: text }; }
  if (!res.ok) throw new Error(`${method} ${path} -> ${res.status}: ${text.slice(0, 500)}`);
  return json.data ?? json.message ?? json;
}

async function main() {
  console.log("🚀 CREATING PROGRAM & COURSE ENROLLMENTS FOR FATHIMA RUFAIDHA M M");
  console.log("=".repeat(70));
  
  const STUDENT_ID = "STU-SU ERV-26-204";
  const STUDENT_NAME = "FATHIMA RUFAIDHA M M";
  const PROGRAM = "8th State";
  const ACADEMIC_YEAR = "2026-2027";
  const ENROLLMENT_DATE = "2026-06-01";
  const SRR_ID = "ERV-204";
  const BATCH_NAME = "Eraveli 26-27";
  
  const COURSES = [
    "8th Language2",
    "8th Social Science",
    "8th Hindi",
    "8th IT",
    "8th Language1",
    "8th Mathematics",
    "8th English",
    "8th Biology",
    "8th Physics",
    "8th Chemistry",
    "8th Malayalam",
  ];

  // Create Program Enrollment with custom_student_srr = "ERV-204" to avoid collision with FKO student
  console.log(`\n📝 Step 1: Creating Program Enrollment...`);
  const pePayload = {
    doctype: "Program Enrollment",
    student: STUDENT_ID,
    student_name: STUDENT_NAME,
    custom_student_srr: SRR_ID,
    program: PROGRAM,
    academic_year: ACADEMIC_YEAR,
    enrollment_date: ENROLLMENT_DATE,
    student_batch_name: BATCH_NAME,
    custom_plan: "Basic",
    custom_no_of_instalments: "8",
    custom_fee_structure: "SU ERV-8th State-Basic-8",
    courses: COURSES.map((c) => ({ course: c })),
  };

  const createdPE = await apiCall("POST", "/api/resource/Program Enrollment", pePayload);
  console.log(`  ✓ Created Program Enrollment: ${createdPE.name}`);

  // Submit Program Enrollment
  await apiCall("PUT", `/api/resource/Program Enrollment/${encodeURIComponent(createdPE.name)}`, { docstatus: 1 });
  console.log(`  ✓ Submitted Program Enrollment: ${createdPE.name}`);

  // Verify Course Enrollments auto-generated by Frappe
  const ceList = await apiCall(
    "GET",
    `/api/resource/Course Enrollment?filters=${encodeURIComponent(JSON.stringify([["student", "=", STUDENT_ID]]))}&fields=${encodeURIComponent(JSON.stringify(["name", "course", "program_enrollment"]))}`
  );

  console.log("\n" + "=".repeat(70));
  console.log(`🎉 ENROLLMENT CREATION COMPLETED SUCCESSFULLY FOR FATHIMA RUFAIDHA M M!`);
  console.log("=".repeat(70));
  console.log(`Summary:`);
  console.log(`  Program Enrollment: ${createdPE.name} (Status: Submitted)`);
  console.log(`  Auto-created Course Enrollments (${ceList.length} courses):`);
  ceList.forEach((ce) => console.log(`   • ${ce.name} (${ce.course})`));
}

main().catch((err) => {
  console.error(`\n❌ FAILED: ${err.message}`);
  process.exit(1);
});
